
class style:
    def __init__(self, color):
        self.color= color
    def get_color(self):
        if self.color ==4:
            return "#FFFBF3"#labels
        elif self.color ==3:
            return "#EDF9FF"#Background
        elif self.color ==1:
            return "#FF9D4C"#Boton Hover
        elif self.color ==2:
            return "#FFFBF3"#Background Treeview 
        elif self.color ==5:
            return "#48B2E5"#Treeview color
        elif self.color ==6:
            return "#48B2E5"#Botones
        elif self.color ==7:
            return "#FFFBF3"#BG Entrys  y labels

